document.addEventListener('keydown', function(event) {
    if (event.key === '1') {
        window.location.href = 'https://pufferhost.pl/';
    }
    if (event.key === '2') {
        window.location.href = 'https://pufferhost.pl/about.html';
    }
    if (event.key === '3') {
        window.location.href = 'https://pufferhost.pl/articles/index.html';
    }
});